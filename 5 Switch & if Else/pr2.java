// WAPTP  REMARK ACCORDING TO THEIR RESPECTIVE GRADE    ( O => Outstanding       A => Excelent)

class P2{
	public static void main(String[] args){
		char ch = 'O';
		if (ch == 'O'){
			System.out.println(" Outstanding ");
		}
		else if (ch == 'A'){
			System.out.println(" Excelent ");
		}
		else {
			System.out.println("Invalid data " );
		}
	}
}

