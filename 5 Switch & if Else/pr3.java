// WAPTP longform of cloth sizes according to their acronym


class P3{
	public static void main(String []args){
		char ch = 'S';
		if (ch == 'L'){
			System.out.println( " large" );
		}
		else if (ch == 'M'){
			System.out.println(" medium ");
		}
		else if (ch == 'S'){
			System.out.println(" Small");
		}
		else {
			System.out.println("Invalid data ");
		}
	}
}

